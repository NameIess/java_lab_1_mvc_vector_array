package by.bntu.fitr.isit.javalabs.lab1.view;

public class CreateVectorView {
    private int size;
    private int from;
    private int to;

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }
}
